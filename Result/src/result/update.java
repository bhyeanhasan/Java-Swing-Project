    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
       
        connection conn = new connection();
        try {
            con = conn.connect();
        } 
        catch (SQLException ex) {
            System.out.println(ex);
        }
        
        String q = "update datatable set Name=?, Math=?, Physics=?, Chemistry=?,Biology=?,GK=?,English=? where ID=?";
        
        try
        {
            pst = con.prepareStatement(q);
            
            pst.setString(1, id.getText());
            pst.setString(2, name.getText());
            pst.setString(3, math.getText());
            pst.setString(4, phy.getText());
            pst.setString(5, che.getText());
            pst.setString(6, bio.getText());
            pst.setString(7, gk.getText());
            pst.setString(8, eng.getText());
            pst.setInt(9,Integer.parseInt(math.getText())+Integer.parseInt(phy.getText())+Integer.parseInt(che.getText())+Integer.parseInt(bio.getText())+Integer.parseInt(gk.getText())+Integer.parseInt(eng.getText()));

            pst.execute();
            con.close();
        }
        catch (Exception e)
        {
            System.out.println(e);
        }
        
    }//GEN-LAST:event_jToggleButton1ActionPerformed

